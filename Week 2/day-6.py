"""
التحويل من نوع إلى نوع آخر في لغة البايثون
"""
print("--------------------------")
# التحوي الى رقم صحيح

x = int(10.9)
y = int(10.2)
z = int("10")

print(x)
print(y)
print(z)
print("--------------------------")

# التحويل الى رقم عشري

x = float(10)
y = float(10.2)
z = float("10")
x = float("10.5")
print(x)
print(y)
print(z)
print(x)
print("--------------------------")

# التحويل الى نص

x = str(10)
y = str(10.2)
z = str(2)
x = str(6)
print(x)
print(y)
print(z)
print(x)
print("--------------------------")